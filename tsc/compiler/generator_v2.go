// foundryTypesTemplate generates the foundry/types.go file with HookContext and
// all request/result types used by hook function signatures. This must stay in
// sync with tsc/spec/foundry/types.go (the canonical source of truth).
var foundryTypesTemplate = template.Must(template.New("foundry_types").Parse(`// Code generated by forge compile. DO NOT EDIT.
// Foundry hook shared types — implementing docs/HOOKS_CONTRACT.md
// Canonical source: github.com/jsell-rh/trusted-software-foundry/tsc/spec/foundry
package foundry

import (
	"context"
	"net/http"
	"time"
)

// HookContext is passed to every hook function. It carries operation context,
// a structured logger, and request-scoped metadata.
type HookContext struct {
	Ctx       context.Context
	Logger    Logger
	Tracer    Tracer
	AppName   string
	TenantID  string
	RequestID string
	TraceID   string
	Claims    map[string]any
}

// Logger is a minimal structured logging interface. Arguments are alternating
// key-value pairs: Info("msg", "key1", "val1", "key2", "val2").
type Logger interface {
	Info(msg string, keyvals ...string)
	Warn(msg string, keyvals ...string)
	Error(msg string, keyvals ...string)
	Debug(msg string, keyvals ...string)
}

// Tracer wraps OpenTelemetry tracing.
type Tracer interface {
	Start(ctx context.Context, spanName string) (context.Context, Span)
}

// Span is an OpenTelemetry span.
type Span interface {
	End()
	SetAttribute(key string, value any)
	RecordError(err error)
}

// PostHandlerRequest carries the response details available after the handler runs.
type PostHandlerRequest struct {
	// Method and Path of the handled request.
	Method string
	Path   string

	// StatusCode is the HTTP status code that was written.
	StatusCode int

	// ResponseBytes is the number of bytes written to the response body.
	ResponseBytes int64

	// Duration is the time taken by the handler (excluding this hook).
	Duration time.Duration

	// RequestHeaders are the incoming request headers.
	RequestHeaders http.Header
}

// DBOperation describes a database operation about to execute.
type DBOperation struct {
	Resource   string
	Type       string         // "create", "read", "update", "delete", "list"
	ResourceID string
	Payload    map[string]any // mutable for create/update
	Filter     map[string]any
}

// DBResult carries the outcome of a completed database operation.
type DBResult struct {
	Resource     string
	Operation    string
	ID           string
	RowsAffected int64
	Error        error
}

// EventMessage is the message being published to the event bus.
type EventMessage struct {
	Topic   string
	Key     string
	Payload []byte
	Headers map[string]string
}

// ConsumedEvent is the event received from the event bus.
type ConsumedEvent struct {
	Topic   string
	Key     string
	Payload map[string]any // pre-parsed JSON payload
	Headers map[string]string
	Offset  int64
}
`))

func (g *Generator) writeFoundryTypes(ir *spec.IRSpec) error {
	if len(ir.Hooks) == 0 {
		return nil
	}
	foundryDir := filepath.Join(g.outputDir, "foundry")
	if err := os.MkdirAll(foundryDir, 0755); err != nil {
		return fmt.Errorf("creating foundry/ dir: %w", err)
	}
	var buf bytes.Buffer
	if err := foundryTypesTemplate.Execute(&buf, nil); err != nil {
		return fmt.Errorf("rendering foundry/types.go: %w", err)
	}
	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		return fmt.Errorf("formatting foundry/types.go: %w", err)
	}
	return os.WriteFile(filepath.Join(foundryDir, "types.go"), formatted, 0644)
}

// hookRegistryTemplate generates the call site dispatch code in hook_registry.go.
// Each hook entry produces a typed call site matching the HOOKS_CONTRACT.md signatures.
var hookRegistryTemplate = template.Must(template.New("hook_registry").Funcs(template.FuncMap{
	"funcName": hookFuncName,
	"callSite": hookCallSite,
}).Parse(`// Code generated by forge compile. DO NOT EDIT.
// Hook registry — call sites for: {{ .IR.Metadata.Name }} v{{ .IR.Metadata.Version }}
// Human hook implementations are in hooks/*.go — never edit call sites.
// Implements docs/HOOKS_CONTRACT.md (frozen by TSC-Architect).
package main

import (
	"net/http"

	"github.com/jsell-rh/trusted-software-foundry/foundry"
	"{{ .AppModule }}/hooks"
)

{{ range .Hooks }}// {{ funcName . }} is the generated call site for hook "{{ .Name }}" at point {{ .Point }}.
// Implementation file: {{ .Implementation }}
func {{ funcName . }}({{ callSite . "sig" }}) error {
	return hooks.{{ funcName . }}({{ callSite . "call" }})
}

{{ end }}
`))

// hookFuncName returns the PascalCase function name for a hook.
// Hook name "audit-log" + point "pre-handler" → "AuditLogPreHandler"
func hookFuncName(h spec.IRHook) string {
	name := toPascalCase(h.Name)
	point := toPascalCase(h.Point)
	return name + point
}

// hookCallSite returns the parameter signature or argument list for a hook call site.
// mode "sig" = parameter declarations; mode "call" = argument list.
func hookCallSite(h spec.IRHook, mode string) string {
	switch h.Point {
	case "pre-handler":
		if mode == "sig" {
			return "hctx *foundry.HookContext, w http.ResponseWriter, r *http.Request"
		}
		return "hctx, w, r"
	case "post-handler":
		if mode == "sig" {
			return "hctx *foundry.HookContext, req *foundry.PostHandlerRequest"
		}
		return "hctx, req"
	case "pre-db":
		if mode == "sig" {
			return "hctx *foundry.HookContext, op *foundry.DBOperation"
		}
		return "hctx, op"
	case "post-db":
		if mode == "sig" {
			return "hctx *foundry.HookContext, result *foundry.DBResult"
		}
		return "hctx, result"
	case "pre-publish":
		if mode == "sig" {
			return "hctx *foundry.HookContext, msg *foundry.EventMessage"
		}
		return "hctx, msg"
	case "post-consume":
		if mode == "sig" {
			return "hctx *foundry.HookContext, event *foundry.ConsumedEvent"
		}
		return "hctx, event"
	default:
		if mode == "sig" {
			return "hctx *foundry.HookContext"
		}
		return "hctx"
	}
}

type hookRegistryData struct {
	IR        *spec.IRSpec
	Hooks     []spec.IRHook
	AppModule string
}

func (g *Generator) writeHookRegistry(ir *spec.IRSpec, appModule string) error {
	if len(ir.Hooks) == 0 {
		return nil
	}
	data := hookRegistryData{
		IR:        ir,
		Hooks:     ir.Hooks,
		AppModule: appModule,
	}
	var buf bytes.Buffer
	if err := hookRegistryTemplate.Execute(&buf, data); err != nil {
		return fmt.Errorf("rendering hook_registry.go: %w", err)
	}
	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		return fmt.Errorf("formatting hook_registry.go: %w\nsource:\n%s", err, buf.String())
	}
	return os.WriteFile(filepath.Join(g.outputDir, "hook_registry.go"), formatted, 0644)
}

