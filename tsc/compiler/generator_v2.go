// toPascalCase converts a kebab-case name to PascalCase.
// "api-server" → "ApiServer", "worker" → "Worker"
func toPascalCase(s string) string {
	parts := strings.Split(s, "-")
	var sb strings.Builder
	for _, p := range parts {
		if len(p) > 0 {
			sb.WriteString(strings.ToUpper(p[:1]) + p[1:])
		}
	}
	return sb.String()
}

// toSnakeCase converts a kebab-case name to snake_case for file names.
// "api-server" → "api_server"
func toSnakeCase(s string) string {
	return strings.ReplaceAll(s, "-", "_")
}

var serviceMainTemplate = template.Must(template.New("svcmain").Funcs(template.FuncMap{
	"cleanAlias": func(name string) string {
		return strings.ReplaceAll(name, "-", "")
	},
	"pascal": toPascalCase,
}).Parse(`// Code generated by forge compile. DO NOT EDIT.
// Service: {{ .Service.Name }} (role: {{ .Service.Role }})
// Spec: {{ .IR.Metadata.Name }} v{{ .IR.Metadata.Version }}
package main

import (
	"context"
	"fmt"
	"os"
	"os/signal"
	"syscall"

	"github.com/jsell-rh/trusted-software-foundry/tsc/spec"
{{ range .Components }}	{{ cleanAlias .Name }} "{{ .Module }}"
{{ end }})

func main{{ pascal .Service.Name }}() {
	resources := []spec.ResourceDefinition{}
	app := spec.NewApplication(resources)
{{ range .Components }}	app.AddComponent({{ cleanAlias .Name }}.New())
{{ end }}
	configs := map[string]spec.ComponentConfig{}
	if err := app.Configure(configs); err != nil {
		fmt.Fprintf(os.Stderr, "configure: %v\n", err)
		os.Exit(1)
	}
	if err := app.Register(); err != nil {
		fmt.Fprintf(os.Stderr, "register: %v\n", err)
		os.Exit(1)
	}

	ctx, cancel := signal.NotifyContext(context.Background(), syscall.SIGINT, syscall.SIGTERM)
	defer cancel()

	if err := app.Run(ctx); err != nil {
		fmt.Fprintf(os.Stderr, "run: %v\n", err)
		os.Exit(1)
	}
}
`))

type serviceMainData struct {
	IR         *spec.IRSpec
	Service    spec.IRService
	Components []ResolvedComponent
}

func (g *Generator) writeServiceMains(ir *spec.IRSpec, components []ResolvedComponent) error {
	if len(ir.Services) == 0 {
		return nil
	}

	// Build component lookup by name for filtering per-service components.
	compByName := map[string]ResolvedComponent{}
	for _, c := range components {
		compByName[c.Name] = c
	}

	for _, svc := range ir.Services {
		// Select components for this service (subset or all).
		var svcComponents []ResolvedComponent
		if len(svc.Components) == 0 {
			svcComponents = components
		} else {
			for _, name := range svc.Components {
				if c, ok := compByName[name]; ok {
					svcComponents = append(svcComponents, c)
				}
			}
		}
		svcComponents = sortComponents(svcComponents)

		data := serviceMainData{
			IR:         ir,
			Service:    svc,
			Components: svcComponents,
		}

		var buf bytes.Buffer
		if err := serviceMainTemplate.Execute(&buf, data); err != nil {
			return fmt.Errorf("rendering main_%s.go: %w", svc.Name, err)
		}
		formatted, err := format.Source(buf.Bytes())
		if err != nil {
			return fmt.Errorf("formatting main_%s.go: %w\nsource:\n%s", svc.Name, err, buf.String())
		}
		filename := "main_" + toSnakeCase(svc.Name) + ".go"
		if err := os.WriteFile(filepath.Join(g.outputDir, filename), formatted, 0644); err != nil {
			return fmt.Errorf("writing %s: %w", filename, err)
		}
	}
	return nil
}

